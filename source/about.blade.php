    <p class="mb-6">Hello there. My name's Daniel and I've been breaking computers since the '90s. Fixing them took a little while longer. I put this site together to house articles and serve as a technical showcase for various things.</p>

    <p class="mb-6">My computing journey started in 1995 with a Packard Bell Legend, with 8MB of RAM and Windows 3.11. I had a fascination with the Web almost immediately. My first paid work was in 2004 doing break-fix, house-call stuff in rural Kentucky where computers were still a novelty, and I was hooked.</p>

    <p class="mb-6">In 2005 I decided to open my own repair and sales shop while still attending high school, and rented a storefront next door to the local Radio Shack in a little town, population 800-ish. I lost my butt on that place, but the lessons learned were invaluable, and in 2006 I attended the University of Louisville for Computer Engineering and Computer Science.</p>

    <p class="mb-6">As it turns out, you need both discipline <i>and</i> money to attend college regularly, and at 18 I lacked both, so that didn't last long. I did tech sales for a while instead, working at Office Depot and Best Buy for the better part of six years, not counting a stretch where I worked as a PHP developer, MSSQL administrator, and sysadmin.</p>

    <p class="mb-6">In 2011 I moved into the MSP world as a technician and then a service manager, working with a lot of Windows Server environments, and in 2014 set up my first AWS account and began nibbling around the edges of the cloud and all of its potential.</p>
        
    <p class="mb-6">By 2015 I was working with Hyperconverged infrastructure for state government, where observability and monitoring were key skills to pick up, and then spent a few years as a CTO for the largest privately-owned for-profit in New Mexico.</p>

    <p class="mb-6">These days I work as a consultant, using this strange blend of skills to surprisingly good effect as a Site Reliability Engineer. I earned my AWS Solutions Architect Professional certification in 2020 and my future goals revolve around Kubernetes and its ecosystem.</p>

    <p class="mb-6">When I'm not working, I enjoy writing, professional wrestling, playing bass guitar, and playing video games. I collect retro video games from the DOS and NES era onward, and I have a deep interest in the fragrance world and industry. I also do occasional voice work as a narrator, voice actor, and podcaster.</p>


